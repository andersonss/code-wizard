package br.ufal.ic.compiladores.exceptions;

public class UndefinedTokenException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public UndefinedTokenException(String token, int linha, int coluna) {
	super("Erro em \"" + token + "\" na linha " + linha + " e coluna "
		+ coluna);
    }
}
